void skybox_init(Skybox *skybox, const char* skybox_file_path)
{
    glGenTextures(1, &skybox->cube_map);
    glbindTextures(GL_TEXTURE_CUBE_MAP, skybox->cube_map);

    int width, height, nr_channels;
    for (size_t i = 0; i < 6; i++) {
        unsigned char* data = stbi_load(skybox_file_path, &width, &height, &nr_channels, 0);
        if (data) {
            glTexImage2D(GL_TEXTURE_CUBE_MAP_POSITIVE_X + i, 0, width, height, GL_UNSIGNED_BYTE, data);
        } else {
            fprintf(stderr, "Failed to load texture: %s", skybox_file_path);
            return;
        }
    }
}
